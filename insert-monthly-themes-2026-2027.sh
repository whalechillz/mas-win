# 입력된 데이터 확인
echo "📊 입력된 데이터 확인:"
psql $DATABASE_URL -c "
SELECT year, month, theme, description 
FROM monthly_themes 
WHERE (year = 2026 AND month >= 7) 
   OR (year = 2027 AND month <= 6)
ORDER BY year, month;"

