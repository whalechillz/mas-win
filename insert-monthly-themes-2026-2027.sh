#!/bin/bash
# 2026년 7월 ~ 2027년 6월 월별 테마 데이터 입력 스크립트

echo "🔄 2026년 7월 ~ 2027년 6월 월별 테마 데이터 입력 시작..."

# 환경 변수에서 데이터베이스 정보 읽기
source .env.local

# SQL 실행
psql $DATABASE_URL -f database/monthly-themes-2026-07-to-2027-06.sql

